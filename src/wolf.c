void	*ft_wolf(void *tab)
{
	t_indication id;

	id = *(t_indication *)tab;
	while (id.x < id.x_next)
	{
		ft_p1(&id);
		ft_p2(&id);
		ft_p3(&id);
		ft_p4(&id);
		ft_show(&id);
		ft_show_other(&id);
		id.x++;
	}
	return (0);
}

int		ft_exit(void)
{
	exit(0);
	return (0);
}

int		main(int ac, char **av)
{
	t_indication	id;

	if (ac == 1)
	{
		id.mlx = mlx_init();
		id.win = mlx_new_window(id.mlx, VARX, VARY, "WOLF 3D !");
		ft_init(&id);
		mlx_loop_hook(id.mlx, set_image, &id);
		mlx_hook(id.win, 2, 0, keypress, &id);
		mlx_hook(id.win, 3, 0, keyrelease, &id);
		mlx_hook(id.win, 17, 18, ft_exit, NULL);
		mlx_loop(id.mlx);
	}
	else
		ft_putstr("Erreur : usage\n");
	return (0);
}
